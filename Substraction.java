class Substraction{
	
	public static void substractionOfTwoNumbers(int x, int y){
		
		System.out.println("subtraction method started");
		int result = (x-y);
		System.out.println("Result =" +result);
		System.out.println("subtraction method ended");
	
	}
	
	public static void main(String a[]){
		
		System.out.println("main method started");
		substractionOfTwoNumbers(467, 987);
		substractionOfTwoNumbers(56, 45);
		System.out.println("main method ended");
	
	
	}

}