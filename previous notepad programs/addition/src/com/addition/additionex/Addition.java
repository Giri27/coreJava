package com.addition.additionex;

public class Addition {
	
	public static void additionOfTwoNos(int x, int y) {
		
		int result = x+y;
		System.out.println("addition of two numbers method started");
		System.out.println("addition of two numbers : " +result);
		System.out.println("addition of two numbers method ended");
	}
	
	public static void additionOfThreeNos(int p, int q, int r) {
		
		int result = p+q+r;
		System.out.println("addition of three numbers method started");
		System.out.println("addition of three numbers : " +result);
		System.out.println("addition of three numbers method ended");
	}
	

}
